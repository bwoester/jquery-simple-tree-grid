goog.provide('bwoester.simpleTreeGrid.RowData');

goog.require('bwoester.Ternary');



///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////



/**
 * @constructor
 */
bwoester.simpleTreeGrid.RowData = function() {
}

///////////////////////////////////////////////////////////////////////////////

/**
 * @public
 * @type {bwoester.TreeNode}
 */
bwoester.simpleTreeGrid.RowData.prototype.dataNode = null;

///////////////////////////////////////////////////////////////////////////////

/**
 * @public
 * @type {bwoester.Ternary}
 */
bwoester.simpleTreeGrid.RowData.prototype.expanded = bwoester.Ternary.UNKNOWN;

///////////////////////////////////////////////////////////////////////////////

/**
 * @public
 * @type {string}
 */
bwoester.simpleTreeGrid.RowData.prototype.modelIdProperty = 'id';

///////////////////////////////////////////////////////////////////////////////

/**
 * @public
 */
bwoester.simpleTreeGrid.RowData.prototype.getId = function()
{
  var model = this.dataNode.getValue();
  return model[ this.modelIdProperty ];
}

